	// check if an in world position is out of the map bounds
	outOfBounds(x, y) {
		return x < 0 || x >= this.walls[0].length || y < 0 || y >= this.walls.length; 
	}

	// get the wall cell value of a ray intersection
	getWallCellValue(x, y, angle, vertical) {
		const facingUp = Math.abs(Math.floor(angle / Math.PI) % 2);
		const facingLeft = Math.abs(Math.floor((angle - Math.PI / 2) / Math.PI) % 2);
		const yCell = facingUp && !vertical ? Math.floor(y / this.CELL_SIZE) - 1 : Math.floor(y / this.CELL_SIZE);
		const xCell = !facingLeft && vertical ? Math.floor(x / this.CELL_SIZE) - 1 : Math.floor(x / this.CELL_SIZE);
		return this.walls[yCell][xCell];
	}

	// get the floor cell value of a ray intersection
	getFloorCellValue(x, y) {
		const yCell = Math.floor(y / this.CELL_SIZE);
		const xCell = Math.floor(x / this.CELL_SIZE);
		return this.floor[yCell][xCell];
	}

	// get the ceiling cell value of a ray intersection
	getCeilingCellValue(x, y) {
		const yCell = Math.floor(y / this.CELL_SIZE);
		const xCell = Math.floor(x / this.CELL_SIZE);
		return this.ceiling[yCell][xCell];
	}
